void cinco_peaks(uint16_t* data_array, int array_len, double mult)
{   
    int peaks = 5;
    double value;
    double peaksArr[peaks];
    for (int i=0; i < peaks; i++) {
        peaksArr[i] = -INFINITY;
    }
    
    for (int i = 0; i < array_len; i++)
    {
        value = (int16_t)data_array[i] * mult;
        for (int j=0; j < peaks; j++)
        {                            //   7
            if (value > peaksArr[j]) // 8 6 2 1 0
            {
                for (int k=peaks-1; k>j; k--)
                {
                    peaksArr[k] = peaksArr[k - 1];
                }
                peaksArr[j] = value;
                break;
            }
        }
    }
    // printf("Peaks: ");
    for (int i=0; i < peaks-1; i++)
    {
        printf("%f, ", peaksArr[i]);
    }
    printf("%f; ", peaksArr[peaks-1]);
}

void RMS(double *rms, uint16_t* data_array, int array_len)
{
    float sum = 0;
    for (int i = 0; i < array_len; i++) {
        sum += pow(data_array[i], 2);
        rms[i] = sqrt(sum/ (i + 1));
    }

    for (int i=0; i < array_len-1; i++)
    {
        printf("%f, ", rms[i]);
    }
    printf("%f; ", rms[array_len-1]);
}

        
